namespace QuickerActionPanel.Models
{
    /// <summary>
    /// Layout modes for displaying action items
    /// </summary>
    public enum LayoutMode
    {
        /// <summary>
        /// Small icon with text (horizontal layout)
        /// </summary>
        IconAndText = 0,

        /// <summary>
        /// Big icon with text below (vertical layout)
        /// </summary>
        BigIcon = 1,

        /// <summary>
        /// Icon only
        /// </summary>
        IconOnly = 2
    }
}

